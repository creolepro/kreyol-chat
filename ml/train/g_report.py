def _latency_note(depth):
    p = M.param_count(depth)["total"]
    return f"{p/1e6:.0f}M params → ~{depth} transformer blocks; CPU/browser decode latency scales ≈ linearly with depth"


def sweep_md():
    r = _load("g_sweep_results.json")
    assert r, "no g_sweep_results.json — run `g_run sweep` first"
    runs = r["runs"]
    A = []
    A.append("# Depth mini-sweep — Model C (d12 / d16 / d20)")
    A.append("")
    S = r["sweep_config"]
    tok = S["num_iterations"] * G.TOKENS_PER_STEP
    A.append(f"*Snapshot {G.SNAPSHOT_DATE}. Identical data order + budget: **{S['num_iterations']} steps × "
             f"{G.TOKENS_PER_STEP:,} tok/step = {tok/1e6:.0f}M tokens** each, seed {G.TRAIN['seed']}. "
             f"GPU: Modal {F.MODAL_GPU}. Generated by `train/g_report.py`.*")
    A.append("")
    A.append("Data-limited (112M unique tokens) + local-first: the pick is the depth where validation BPB "
             "stops improving enough to justify the per-token inference cost — not the largest model.")
    A.append("")
    A.append("| depth | params | median tok/s | final train loss | BPB general ↓ | BPB authored ↓ | BPB transl. |")
    A.append("|--:|--:|--:|--:|--:|--:|--:|")
    for d in S["depths"]:
        run = runs.get(f"d{d}")
        if not run:
            continue
        tr = run["train"]; bp = run["bpb"]
        gen = bp.get("general_holdout", {}).get("bpb")
        auth = bp.get("authored_eval", {}).get("bpb")
        trs = bp.get("translation_shaped_eval", {}).get("bpb")
        loss = tr["logs"][-1]["loss"] if tr.get("logs") else None
        A.append(f"| d{d} | {tr['params']/1e6:.0f}M | {tr.get('median_tok_s')} | {loss} | "
                 f"{gen:.4f} | {auth:.4f} | {trs:.4f} |" if gen else
                 f"| d{d} | {tr['params']/1e6:.0f}M | {tr.get('median_tok_s')} | {loss} | — | — | — |")
    A.append("")
    A.append("## Pick")
    A.append("")
    A.append("_(rationale filled from the numbers above — see the FINAL SUMMARY / phase-1.md G block.)_")
    A.append("")
    A.append("Latency context per depth:")
    for d in S["depths"]:
        A.append(f"- **d{d}**: {_latency_note(d)}.")
    _w(os.path.join(REPORTS, "depth_sweep.md"), A)


