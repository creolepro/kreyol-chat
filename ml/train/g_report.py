"""Generate the Workstream-G reports from the results JSONs written by g_run.py:

  f2_gates.md     — Part 2: gate-by-gate evidence.
  depth_sweep.md  — Part 3: sweep BPB table + depth pick + rationale.
  modelc_v0.md    — Part 4: learning curves, checkpoint story, vs-bases table, nutrition label.

Run:  python -m train.g_report {gates|sweep|modelc|all}
"""

from __future__ import annotations

import argparse
import json
import os

from . import config as F
from . import llama_config as G
from . import llama_model as M

REPORTS = os.path.join(F.REPO_ROOT, "reports")


def _load(name):
    p = os.path.join(G.G_WORK, name)
    return json.load(open(p, encoding="utf-8")) if os.path.exists(p) else None


def _w(path, lines):
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")
    print(f"[g_report] wrote {path}")


# ============================ f2_gates.md =====================================

def gates_md():
    r = _load("g_gates_results.json")
    assert r, "no g_gates_results.json — run `g_run gate` first"
    g = r["gates"]
    A = []
    A.append("# F2 conversion gates — Model C (standard-Llama)")
    A.append("")
    A.append(f"*Snapshot {G.SNAPSHOT_DATE}. Throwaway **d16** checkpoint (`{g['tag']}` step {g['step']}), "
             f"trained only to exercise the deployment chain. llama.cpp pinned `{g['llama_cpp_commit'][:12]}`. "
             f"GPU: Modal {F.MODAL_GPU}. Generated by `train/g_report.py`. See "
             f"[../../docs/phase-1.md](../../docs/phase-1.md) Workstream G.*")
    A.append("")
    A.append("The Workstream-F finding was that nanochat's speedrun architecture had no llama.cpp "
             "graph (48% nanochat-only params) and its custom pre-tokenizer wasn't registered. Model C "
             "is now a **real `transformers.LlamaForCausalLM`**, so the architecture blocker is gone by "
             "construction; these gates prove the *rest* of the chain end-to-end.")
    A.append("")
    # verdict table
    def _pf(b):
        return "✅ pass" if b else "❌ fail"
    g1 = g["gate1_logits"]; g2 = g["gate2_gguf"]
    g4 = g.get("gate4_parity", {}); g3 = g.get("gate3_stock", {})
    g5 = g["gate5_onnx"]; g6 = g.get("gate6_cross_runtime", {})
    A.append("## Verdict")
    A.append("")
    A.append("| Gate | What it proves | Result |")
    A.append("|---|---|---|")
    A.append(f"| 1 native↔HF logits | export is lossless | {_pf(g1['pass'])} — fp32 round-trip Δ={g1['max_abs_logit_diff_fp32_roundtrip']:.1e}, bf16 Δ={g1['max_abs_logit_diff_fp32_vs_bf16']:.1e} |")
    A.append(f"| 2 GGUF + llama.cpp | converts & generates Kreyòl | {_pf(g2['final_rc'] == 0 and g2['gguf_exists'])} — rc={g2['final_rc']}, registered `kreyol-bpe`={g2['registered_kreyol_bpe']} |")
    if g4:
        p = g4["probe"]
        A.append(f"| 4 token-ID parity | tiktoken=HF=llama.cpp | {_pf(p['llamacpp_sentence_frac'] > 0.99 and g4['fixtures_all_match'])} — llama.cpp {p['llamacpp_sentence_frac']:.3f} / HF {p['hf_sentence_frac']:.3f} sentence-exact |")
    if g3:
        rej = g3.get("ollama_rejected_unknown_pretokenizer")
        A.append(f"| 3 stock Ollama | measured, not faked | {'⚠️ rejects (as predicted)' if rej else _pf(g3.get('ollama_loaded_and_generated'))} — see below |")
    A.append(f"| 5 ONNX browser | transformers.js path | {_pf(g5.get('export_ok') and g5.get('onnx_gen_ok'))} — export_ok={g5.get('export_ok')}, gen_ok={g5.get('onnx_gen_ok')} |")
    if g6:
        pre = g6.get("pre_quant", [])
        avg = round(sum(x["native_vs_llamacpp_lcp"] for x in pre) / len(pre), 3) if pre else None
        A.append(f"| 6 cross-runtime greedy | native=llama.cpp=ONNX | native↔llama.cpp LCP avg={avg} (pre-quant); Q4 looser |")
    A.append("")

    # gate 1
    A.append("## Gate 1 — native vs exported-HF logits")
    A.append("")
    A.append(g1["note"])
    A.append("")
    A.append(f"- fp32 reload round-trip max|Δlogit| = **{g1['max_abs_logit_diff_fp32_roundtrip']:.2e}** (lossless save/load).")
    A.append(f"- fp32 vs bf16 (deployment dtype) max|Δlogit| = **{g1['max_abs_logit_diff_fp32_vs_bf16']:.2e}**; argmax token agreement = **{g1['argmax_token_agree_fp32_vs_bf16']}**.")
    A.append("")

    # gate 2
    A.append("## Gate 2 — HF → GGUF → llama.cpp (patched)")
    A.append("")
    A.append(f"`convert_hf_to_gguf.py` first pass recognized our pre-tokenizer: "
             f"**{g2['first_pass_recognized_pretokenizer']}**. Harvested chkhsh "
             f"`{g2.get('harvested_chkhsh')}` → registered as `kreyol-bpe` in `conversion/base.py` "
             f"(the generic custom-tokenizer step). Final convert rc=**{g2['final_rc']}**, "
             f"GGUF **{g2['gguf_bytes']/1e6:.1f} MB**.")
    A.append("")
    if g2.get("llama_cpp_generation"):
        gen = g2["llama_cpp_generation"]
        A.append("Greedy generation from the patched `llama-cli` (proves the compiled binary loads our "
                 "`kreyol-bpe` pre-tokenizer and emits Kreyòl):")
        A.append("")
        A.append("```")
        A.append(gen["text"][-600:].strip())
        A.append("```")
        A.append("")

    # gate 4
    if g4:
        A.append("## Gate 4 — token-ID parity (tiktoken / HF / llama.cpp)")
        A.append("")
        p = g4["probe"]
        A.append(f"On the {p['n']}-line source-mixed probe (tiktoken = the rustbpe source of truth; "
                 f"HF leg full, llama.cpp leg on {p.get('llamacpp_n', '?')} lines — llama-tokenize "
                 f"reloads the GGUF per line):")
        A.append("")
        A.append("| pair | sentence-exact | token-level |")
        A.append("|---|--:|--:|")
        A.append(f"| HF tokenizer.json vs tiktoken | {p['hf_sentence_frac']:.4f} | {p['hf_token_frac']:.4f} |")
        A.append(f"| llama.cpp GGUF vs tiktoken | {p['llamacpp_sentence_frac']:.4f} | {p['llamacpp_token_frac']:.4f} |")
        A.append("")
        A.append("**Apostrophe / clitic / accent fixtures** (the cases a wrong pre-tokenizer mangles):")
        A.append("")
        A.append("| fixture | tiktoken n | HF match | llama.cpp match |")
        A.append("|---|--:|:--:|:--:|")
        for fx in g4["fixtures"]:
            A.append(f"| `{fx['text']}` | {len(fx['tiktoken'])} | {'✅' if fx['hf_match'] else '❌'} | "
                     f"{'✅' if fx['llamacpp_match'] else '❌'} |")
        A.append("")
        if p.get("divergence_examples"):
            A.append("First probe divergences (if any):")
            A.append("")
            A.append("```")
            for ex in p["divergence_examples"]:
                A.append(json.dumps(ex, ensure_ascii=False))
            A.append("```")
            A.append("")

    # gate 3
    if g3:
        A.append("## Gate 3 — stock Ollama (measured, not faked)")
        A.append("")
        if g3.get("ollama_bin"):
            cr = g3.get("ollama_create", {}); rn = g3.get("ollama_run", {})
            A.append(f"Stock Ollama (bundles stock llama.cpp) was given the `kreyol-bpe` GGUF via a "
                     f"`FROM` Modelfile. `ollama create` rc={cr.get('rc')}, `ollama run` rc={rn.get('rc')}. "
                     f"Rejected with *unknown pre-tokenizer*: **{g3.get('ollama_rejected_unknown_pretokenizer')}**; "
                     f"loaded+generated: **{g3.get('ollama_loaded_and_generated')}**.")
            A.append("")
            A.append("```")
            A.append((rn.get("tail") or cr.get("tail") or "").strip()[-700:])
            A.append("```")
            A.append("")
            A.append("This matches the llama.cpp source: an unrecognized `tokenizer.ggml.pre` **throws** "
                     "(`unknown pre-tokenizer type`), it does NOT silently fall back to GPT-2 — so a stock "
                     "runtime *refuses* the model rather than mis-tokenizing. The fix is the local "
                     "llama.cpp patch (gates 2/4/6 above use it); the upstream registration patch + parity "
                     "fixtures are prepared (`train/patch_llamacpp_cpp.py` + the converter chkhsh entry) but "
                     "**not** submitted — that PR is the human's to open.")
        else:
            A.append("_Ollama binary unavailable in the container; stock behavior is established from the "
                     "llama.cpp source (unrecognized pre-tokenizer throws)._")
        A.append("")
        if g3.get("gpt2_fallback_clitic_damage"):
            A.append("**Counterfactual GPT-2-fallback damage** (what a silent fallback *would* do to clitics — "
                     "the reason the registration matters):")
            A.append("")
            A.append("| fixture | kreyol_aware tokens | GPT-2-split pieces | differs |")
            A.append("|---|--:|--:|:--:|")
            for d in g3["gpt2_fallback_clitic_damage"]:
                A.append(f"| `{d['text']}` | {d['kreyol_aware_tokens']} | {d['gpt2_fallback_pieces']} | "
                         f"{'⚠️' if d['differs'] else '='} |")
            A.append("")

    # gate 5
    A.append("## Gate 5 — ONNX / transformers.js (offline browser path)")
    A.append("")
    if g5.get("export_ok"):
        A.append(f"Exported with `optimum` to ONNX ({', '.join(g5.get('onnx_files', [])[:8])}). "
                 f"onnxruntime greedy generation: **{g5.get('onnx_gen_ok')}**.")
        A.append("")
        if g5.get("onnx_completion"):
            A.append(f"> ONNX greedy completion: `{g5['onnx_completion'][:200]}`")
            A.append("")
        A.append(g5.get("note", ""))
    else:
        A.append(f"❌ export failed: {g5.get('export_error')}")
    A.append("")

    # gate 6
    if g6:
        A.append("## Gate 6 — cross-runtime greedy agreement")
        A.append("")
        A.append("Longest-common-prefix ratio of greedy continuations (1.0 = identical). Pre-quant we "
                 "expect agreement; Q4 gets the looser numerical check.")
        A.append("")
        A.append("| prompt | native↔llama.cpp LCP |")
        A.append("|---|--:|")
        for x in g6.get("pre_quant", []):
            A.append(f"| `{x['prompt'][:40]}` | {x['native_vs_llamacpp_lcp']} |")
        if "native_vs_onnx_lcp_first_prompt" in g6:
            A.append("")
            A.append(f"native↔ONNX LCP (first prompt) = **{g6['native_vs_onnx_lcp_first_prompt']}**.")
        if g6.get("q4"):
            A.append("")
            A.append("**Q4_K_M (looser check):**")
            A.append("")
            A.append("| prompt | Q4↔f16 LCP |")
            A.append("|---|--:|")
            for x in g6["q4"]:
                A.append(f"| `{x['prompt'][:40]}` | {x['q4_vs_f16_lcp']} |")
        A.append("")

    # artifacts + cost
    art = g.get("artifacts", {})
    A.append("## Artifacts")
    A.append("")
    A.append(f"- GGUF f16: {art.get('gguf_f16', {}).get('bytes', 0)/1e6:.1f} MB, sha256 `{art.get('gguf_f16', {}).get('sha256_16')}`")
    A.append(f"- GGUF Q4_K_M: {art.get('gguf_q4', {}).get('bytes', 0)/1e6:.1f} MB, sha256 `{art.get('gguf_q4', {}).get('sha256_16')}`")
    A.append("- ONNX export dir stored on the Modal Volume (transformers.js bundle).")
    A.append("")
    A.append("All artifacts live on the Modal Volume (nothing under `ml/data/` committed).")
    _w(os.path.join(REPORTS, "f2_gates.md"), A)


