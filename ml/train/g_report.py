def modelc_md():
    r = _load("g_flagship_results.json")
    assert r, "no g_flagship_results.json — run `g_run flagship` first"
    base = _load("g_base_bpb.json")
    nutrition = _load("g_nutrition.json")
    tr = r["train"]
    ckpts = tr["checkpoints"]
    A = []
    A.append(f"# Model C v0 — d{r['depth']} Kreyòl-first base ({tr['params']/1e6:.0f}M)")
    A.append("")
    A.append(f"*Snapshot {G.SNAPSHOT_DATE}. Standard Llama, width 768, depth {r['depth']}. "
             f"Corpus v0.1 train split ({tr['unique_train_tokens']/1e6:.0f}M unique tokens); "
             f"{tr['effective_tokens']/1e6:.0f}M effective tokens = **{tr['epochs']} epochs**. "
             f"kreyol-bpe (24,576). GPU: Modal {F.MODAL_GPU}. Generated by `train/g_report.py`.*")
    A.append("")
    # learning curves
    A.append("## Learning curves (BPB per checkpoint)")
    A.append("")
    A.append("| tokens | step | general ↓ | authored ↓ | translation-shaped | FLORES hat |")
    A.append("|--:|--:|--:|--:|--:|--:|")
    for c in ckpts:
        b = c.get("bpb", {})
        def g(k):
            v = b.get(k)
            return f"{v:.4f}" if isinstance(v, (int, float)) else "—"
        A.append(f"| {c['tokens']/1e6:.0f}M | {c['step']} | {g('general_holdout')} | {g('authored_eval')} | "
                 f"{g('translation_shaped_eval')} | {g('flores_hat')} |")
    A.append("")
    # checkpoint story: generations
    A.append("## The checkpoint-by-checkpoint story (frozen prompt list)")
    A.append("")
    A.append("Greedy completions of the 10 frozen exhibit prompts, archived per checkpoint as the slider "
             "asset (`reports/modelc_v0_generations.json`). A few excerpts showing babble→Kreyòl:")
    A.append("")
    # pick 3 checkpoints: first, a middle, last
    show_steps = [ckpts[0], ckpts[len(ckpts) // 2], ckpts[-1]] if len(ckpts) >= 3 else ckpts
    for c in show_steps:
        A.append(f"**{c['tokens']/1e6:.0f}M tokens (step {c['step']})**")
        A.append("")
        for gsamp in (c.get("generations") or [])[:3]:
            A.append(f"- `{gsamp['prompt'][:40]}` → `{gsamp['completion'][:120]}`")
        A.append("")
    # vs bases
    if base:
        A.append("## Model C vs the base-model scorecard (same slices)")
        A.append("")
        A.append("BPB (↓) on the identical held-out texts — the 150M Kreyòl-first model vs the 3–4B "
                 "multilingual bases (Workstream D):")
        A.append("")
        A.append("| model | params | authored ↓ | general ↓ | translation-shaped |")
        A.append("|---|--:|--:|--:|--:|")
        final = ckpts[-1].get("bpb", {})
        A.append(f"| **Model C v0 (d{r['depth']})** | {tr['params']/1e6:.0f}M | "
                 f"{final.get('authored_eval', float('nan')):.4f} | {final.get('general_holdout', float('nan')):.4f} | "
                 f"{final.get('translation_shaped_eval', float('nan')):.4f} |")
        labels = {"google/gemma-3-4b-pt": "gemma-3-4b-pt", "Qwen/Qwen3-4B-Base": "Qwen3-4B-Base",
                  "meta-llama/Llama-3.2-3B": "Llama-3.2-3B"}
        params = {"google/gemma-3-4b-pt": "4B", "Qwen/Qwen3-4B-Base": "4B", "meta-llama/Llama-3.2-3B": "3B"}
        for repo, slices in base["bases"].items():
            def g(k):
                v = slices.get(k, {}).get("bpb")
                return f"{v:.4f}" if isinstance(v, (int, float)) else "—"
            A.append(f"| {labels.get(repo, repo)} | {params.get(repo, '?')} | {g('authored_eval')} | "
                     f"{g('general_holdout')} | {g('translation_shaped_eval')} |")
        A.append("")
    # nutrition label
    if nutrition:
        A.append("## Nutrition label v1")
        A.append("")
        A.append(_nutrition_block(nutrition, r))
    _w(os.path.join(REPORTS, "modelc_v0.md"), A)

    # also dump the full generations JSON (the committable slider asset)
    gens = {"snapshot_date": G.SNAPSHOT_DATE, "depth": r["depth"], "tag": r["tag"],
            "checkpoints": [{"step": c["step"], "tokens": c["tokens"],
                             "generations": c.get("generations", [])} for c in ckpts]}
    gp = os.path.join(REPORTS, "modelc_v0_generations.json")
    with open(gp, "w", encoding="utf-8") as f:
        json.dump(gens, f, indent=2, ensure_ascii=False)
    print(f"[g_report] wrote {gp}")


def _nutrition_block(n, r):
    L = []
    L.append("```")
    L.append(f"Model C v0 — d{r['depth']} ({r['train']['params']/1e6:.0f}M) Kreyòl-first base")
    L.append(f"  Tokenizer   : kreyol-bpe, 24,576 vocab (kreyol_aware pre-tokenization)")
    L.append(f"  Training    : corpus v0.1, {r['train']['unique_train_tokens']/1e6:.0f}M unique tokens, "
             f"{r['train']['epochs']} epochs ({r['train']['effective_tokens']/1e6:.0f}M effective)")
    comp = n.get("composition_by_origin", {})
    L.append("  Composition (by origin):")
    for k, v in comp.items():
        L.append(f"    {k:<22}: {v['pct']:.1f}%  ({v['docs']:,} docs)")
    genre = n.get("composition_by_genre", {})
    if genre:
        L.append("  Composition (by genre):")
        for k, v in sorted(genre.items(), key=lambda x: -x[1]["pct"])[:8]:
            L.append(f"    {k:<22}: {v['pct']:.1f}%")
    L.append(f"  Known gaps  : {n.get('known_gaps', '')}")
    L.append(f"  Audit rates : {n.get('audit_note', '')}")
    L.append("```")
    return "\n".join(L)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("which", choices=["gates", "sweep", "modelc", "all"])
    args = ap.parse_args()
    if args.which in ("gates", "all"):
        gates_md()
    if args.which in ("sweep", "all"):
        sweep_md()
    if args.which in ("modelc", "all"):
        modelc_md()


if __name__ == "__main__":
    main()
